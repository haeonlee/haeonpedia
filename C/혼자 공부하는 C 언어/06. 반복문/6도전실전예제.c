/*  2 이상의 정수를 입력하여 2부터 입력한 수까지의 모든 소수를 출력합니다.
    단, 한 줄에 5개씩 5칸 간격으로 출력합니다.
    하나의 정수가 소수인지 판단해서 출력하는 과정은 다음과 같습니다.

    1. 일단 소수라고 가정한다.
    2. 2부터 그 정수보다 하나 작은 수까지 하나라도 나누어 떨어지면 가정을 취소한다.
    3. 1의 가정이 2에서 바뀌지 않았으면 그 정수를 출력한다.

    위의 1~3까지의 과정은 하나의 정수에 대해서 소수를 판별하는 과정이고,
    어떤 수까지의 모든 수를 판별하기 위해서는 1~3까지의 과정을 다시 반복합니다.
*/

/*  핵심 정리: 특정 범위를 반복하며 그 내에서 조건을 만족하는지 안 하는지 반복하는 경우라면
    if ~ else가 아닌 판단할 만한 변수(e.g. pn)를 만들어야 한다. */


#include <stdio.h>

int main(void)
{
    int num;
    int i, j;
    int count = 0;
    int count2 = 0;

    printf("2 이상의 정수를 입력하세요 : ");
    scanf("%d\n", &num);    // 오류 원인1. 이렇게 scanf에 \n 문자 넣지 마세요!
                            // 이러면 아예 구동 자체가 안 됩니다.

    for (i=2; i<=num; i++)
    {
        for (j=2; j<i; j++)
        {
            if ((i % j) == 0) // 2부터 num보다 하나 작은 수까지 나눗셈 연산
            {
                break;
                /*  오류 원인2. 이렇게 하면, 2로 나눠지는 놈이 아닌 이상, break 되기 전까지는 반복해서 출력되겠지?
                    예를 들어 25의 경우는 왜 3번 출력됐냐 하면, 5로 나눠지기 전까지 (2, 3, 4) 이렇게 나머지가 0이 아닐 테니까!
                    특히 나는 아래처럼 프린트문을 j-for 안에 둬서 더 그럼 */
            }

            else    //  오류 원인3. 두 번째 for문에 print문까지 넣어서 이것까지 루프가 돌아 버림
                    //  답지처럼 소수는 pn 변수로 관리하고, 출력은 j-for문 밖에서 해야 함
            {
                printf("%5d", i);   // 칸으로 깔끔하게 출력하려면 아예 이렇게
                count++;
                if ((count % 5) == 0) printf("\n"); // 한 줄에 5개씩 5칸으로 출력
            }
        }
    }
        
    return 0;
}

